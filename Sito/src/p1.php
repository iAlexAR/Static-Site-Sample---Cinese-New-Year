  <h1>Il Mito</h1>
  <p>Il mito , molto antico , ha piu' di 4000 anni e nasce <i>grazie</i> ad una creatura , malvagia , spaventosa e gigante , il Nian.<br>Questa creatura maemgna si cibava delle
  teste delle persone. Si ritiene anche che , essendo una creatura con un solo corno (non un unicorno),essa incornava la sua preda prima del <i>pasto</i>.<br>Il Nian usciva allo
  scoperto una volta all'anno e ogni volta che finiva il suo pasto svaniva per un 1 anno intero.<br>Con il passare del tempo, una signora molto anziana e saggia riusci'
  a trovare i punti deboem di questa creatura mitica.<br>Pochissime persone credettero a queste difese e la gente continuo' a morire . . .</p>
