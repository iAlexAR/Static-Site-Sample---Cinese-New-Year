  <h1>BUSTA ROSSA 红包 </h1>
  <p>Per tradizione gli zii, i nonni e/o bisnonni devono dare le buste rosse che sono semplicemente dei soldi che i piu' <i>vecchi<i/> danno, ad esempio: il bisnonno deve<br>
la busta al/la nonno/nonna,zio/zia e nipote; invece il nonno la deve dare la busta allo/a zio/zia e al nipote, e lo zio la deve dare ai nipoti. Una volta ricevuti, nella<br>
notte del capodanno si deve metterla sotto il cuscino, il gesto simboleggia la protezione dai fantasmi e sfortuni in piu' porta fortuna per tutto l'anno nuovo<br>
Negli ultimi decenni il Hongbao viene dato anche al bisnonno e/o il nonno da parte dei piu' giovani per augurare loro di stare in salute.</p>
