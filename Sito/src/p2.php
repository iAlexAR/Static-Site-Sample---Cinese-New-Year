  <h1>L'IMPORTANZA DEL COLORE "ROSSO"</h1>
  <p>All'inizio la gente si vestiva del tutto di rosso per spaventare il Nian pero'<br>
con il passare del tempo e' diventato una tradizione, c'era stato anche la moda del "rosso tradizionele cinese",<br>
ma ormai il rosso era ormai diventato il colore fortunato.<br>
Con il passare del tempo, la moda del vestirsi del tutto di rosso e' svanita, ormai la gente<br>piu' che indossare mutande o calze rosse non fa.
Invece, se il segno del nuovo anno coincide con il tuo segno <i>devi</i> mettere obbligatoriamente qualcosa di rosso addosso<br></p> <hr>
