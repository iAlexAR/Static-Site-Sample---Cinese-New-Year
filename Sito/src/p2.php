  <h1>DUILIAN</h1>
  <p>Duilian e' una poesia che viene incollata sul portone e ha una metrica speciale. I numeri di lettere di destra deve essere<br>
uguale a quella di signistra, e il titolo di solito sono di 2 o 4 ideogrammi. Sono tutte scritte sul foglio rosso,inventate dalle famiglie.<br>
Spesso fanno gare di poesia in famiglia e la poesia <i>migliore</i> viene messa sul portone per tutto l'anno. Apparte il Duilian,<br>
viene messa anche la scritta <strong>福</strong> (fu) su un foglio rosso a forma di rombo. Il significato di questa parola sono tutti gli auguri e benedizioni<br>
che ti portera' la fortuna per tutto l'anno. La scritta viene messa all'incontrario perche' in cinese si dice Dao<br></p>
