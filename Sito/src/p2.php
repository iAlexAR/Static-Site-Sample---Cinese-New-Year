<article align=center>
  <h1>LE LANTERNE ROSSE</h1>
  <p>La gente decora le strade con le lanterne rosse, non solo per spaventere il Nian ma anche per illuminare le strade e <br>
farle sembrare piu' vivaci. All'inizio venivano fatte di carta oleaginosa con dentro una candelain seguito coperti con tessuto tessuto.<br>
Al giorno d'oggi, ormai non si fanno piu' a mano ma la maggiorparte sono elettriche.</p>
