  <h1>DRAGONI</h1>
  <p>Il drago e' il simbolo della Cina, una creatura superiore ad ogni cosa. Infatti gli imperatori passati si consideravano "Il Drago".<br>
Il Nian aveva paura di esso, quindi la gente si travestiva da drago per spaventarlo. Esistono anche varie gare<br>
che si svolgono durante l'ultimo dell'anno dove le coppie, travestite da dragone dovrean affrontare diverse prove</p> 
