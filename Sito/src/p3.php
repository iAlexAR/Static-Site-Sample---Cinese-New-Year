  <h1>Pesce al vapore 鱼 (yu)</h1>
  <p>La pronuncia porta ad un antico detto cinese <i>NianNianYouYu</i> che tradotto significa <i>Ogni anno ti rimane qualcosa in piu'.</i><br>
  Questa e' considerata una forma di augurio a cui il popolo ci tiene molto per questo e' un piatto importante.</p>
