  <h1> Tang Yuan 汤圆 (riso glutinato) </h1>
  <p>Storicamente, il piatto ha subito diverse variazioni di nomi, a partire dall'era dell'imperatore Yongle <br>
  della Dinastia Ming, in cui si chiamava ufficialmente yuanxiao, nome palesemente derivato dal termine cinese <br>
  per indicare la festa delle lanterne. Esso significa letteralmente "prima sera", ad indicare il primo plenilunio <br>
  dopo il Capodanno cinese, il quale accade sempre in un novilunio. Tale nome è tutt'oggi utilizzato a nord della Cina.<br>

Nel Cina del sud, la medesima pietanza assume il diverso nome di tangyuan, oppure tangtuan. Secondo una leggenda, <br>
il nome le fu cambiato durante il regno di Yuan Shikai, dall'anno 1912 al 1916, che disapprovava il nome yuanxiao <br>
in quanto omofono di un altro che significa "rimuovere Yuan" (元宵). Il nome tangyuan significa letteralmente "palle rotonde in zuppa"<br>
mentre tangtuan assume il simile significato di "gnocco rotondo in zuppa". <br><br>
<i>Simbolicamente significa che la famiglia non verra' separata e rimarra' unita.</p>
