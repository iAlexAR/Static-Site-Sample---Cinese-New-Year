  <h1>Nian Gao 年糕 (tortino a base di riso agglutinato)</h1>
  <p>In lingua cinese la parola nian gao ha anche un altro significato: migliorare di anno in anno. <br>
  E' quindi diventato un piatto tipico da consumarsi durante le festività del nuovo anno perché è di <br>
  buon auspicio. Questo piatto ha come ingrediente fondamentale il riso "appiccicoso" o agglutinato, <br>
  che viene lavorato in modo da formare un composto a cui vengono aggiunti zucchero, noci, datteri cinesi <br>
  e foglie di loto.</p> 
