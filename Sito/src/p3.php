  <h1>Ravioli 饺子(jiao zi)</h1>
  <p>Con la loro storia di oltre 1800 anni, i ravioli sono diffusi in tutta la Cina ma sono particolarmente <br>
  popolari nelle regioni del nord presso città come Zhengzhou, Chengde, Dalian, Harbin etc. Di solito <br>
  contengono un ripieno di carne tritata e verdure tagliate finemente mentre l'impasto è tirato in modo <br>
  da essere molto sottile ma elastico e resistente. I ripieni sono vari e cambiano a seconda della provincia,<br>
  ma quelli più comuni sono le carni di maiale o manzo, pesce, gamberetti, uova, tofu e verdure. Possono essere <br>
  bolliti, cotti al vapore o fritti. Non solo sono uno dei piatti che bisogna assolutamente mangiare durante <br>
  le feste dal capodanno cinese, ma sono anche diventati uno dei simboli della cucina cinese stessa, <br>
  nonchè uno dei piatti preferiti dai turisti. Hanno l'aspetto di lingotti d'argento e la tradizione vuole <br>
  che più se ne mangi durante il capodanno più soldi si guadagneranno l'anno successivo. <br>
  La loro realizzazione è molto semplice e quasi tutti i cinesi li sanno cucinare, <br>
  bisogna mescolare la farina con l'acqua e tirare l'impasto in piccoli foglietti sottili, <br>
  infine basta completarli con il ripieno preferito.<br><br>
  <strong>Anche il raviolo ha un profondo significato, indica un forte legame dei membri della famiglia.</strong><br>
  <i>Il ripieno del raviolo indica i vari componenti della famiglia invece l'impasto simboleggia la loro unione</i></p> 
