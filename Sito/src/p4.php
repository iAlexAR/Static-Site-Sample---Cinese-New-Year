<h1>Il Nian 年兽</h1>
<p>
Nella mitologia Cinese il Nian 年兽 , detto anche Nián Shòu e' una creatura mitologica che vive <br>
nei mari oppure in montagna. Il significato della parola <i>Nian</i> e' <i>Anno</i>. <br>
Si narra che questa creatura mitologica esca allo scoperto una volta ogni primavera, periodo del <br>
capodanno cinese a nutrirsi di persone, in specifico delle loro teste.<br>
Le sue prede preferite sono i bambini e le persone che non stanno in famiglia durante quel periodo.<br>
<br>Questa creatura ha delle debolezze o perlomeno delle cose a cui e' sensibile, e sono :<br>
Petardi, tamburi, colore rosso, dragone tradizionale festivo, forti rumori e in tempi contemporanei<br>
vengo usati anche i fuochi d'artificio.<br><br><hr>
